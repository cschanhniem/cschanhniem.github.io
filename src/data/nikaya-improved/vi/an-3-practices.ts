export function buildAn3PracticeContent(
    title: string,
    suttaRef: string,
    middlePracticeBody: string,
): string {
    return `
# ${title}
## ${suttaRef}

Đức Phật dạy:

"Này các Tỷ-kheo, có ba đạo lộ.

Thế nào là ba?

Đạo lộ lún sâu,
đạo lộ thiêu đốt,
và trung đạo.

Và này các Tỷ-kheo, thế nào là đạo lộ lún sâu?

Ở đây, có người nghĩ và thấy như sau:
'Trong các dục không có lỗi.'
Do thấy như vậy, người ấy buông mình rơi xuống trong các dục.

Này các Tỷ-kheo, đó gọi là đạo lộ lún sâu.

Và này các Tỷ-kheo, thế nào là đạo lộ thiêu đốt?

Ở đây, có người sống lõa thể, phóng túng theo các hạnh ép xác, không theo thường nghi, liếm tay cho sạch, đi khất thực không chịu bước tới, không chịu đứng lại, không nhận đồ ăn được mang đến, không nhận đồ ăn làm riêng cho mình, không nhận lời mời đi ăn.

Người ấy không nhận đồ ăn từ nồi chảo, từ ngưỡng cửa, từ chỗ đặt giữa các cây gậy hay giữa các cối giã gạo, không nhận đồ ăn từ người đàn bà có thai, đang cho con bú, hay đang sống chung với đàn ông, không ăn cá thịt, không uống rượu nấu hay rượu men, cũng không uống cháo men.

Người ấy có thể chỉ ăn ở một nhà một miếng, hai nhà hai miếng, cho đến bảy nhà bảy miếng. Có thể sống bằng một bát, hai bát, cho đến bảy bát. Có thể ăn một ngày một bữa, hai ngày một bữa, cho đến bảy ngày một bữa, thậm chí nửa tháng mới ăn một lần.

Người ấy ăn rau, ăn kê, ăn lúa hoang, ăn cám, ăn bột vừng, ăn cỏ, ăn phân bò, sống bằng rễ rừng, trái rừng, hay trái rụng. Người ấy mặc vải gai thô, vải tẩm liệm, áo kết bằng cỏ, vỏ cây, tóc người, đuôi ngựa, hay lông cú.

Người ấy nhổ râu tóc, thường đứng chứ không ngồi, sống theo hạnh ngồi chõ hỏ, nằm trên giường gai, và một ngày ba lần xuống nước để gột sạch tội lỗi. Như vậy, người ấy theo đuổi đủ thứ cách hành hạ và thiêu đốt thân thể.

Này các Tỷ-kheo, đó gọi là đạo lộ thiêu đốt.

Và này các Tỷ-kheo, thế nào là trung đạo?

${middlePracticeBody}

Này các Tỷ-kheo, đó là ba đạo lộ."`
}
